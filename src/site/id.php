      <h1>The License Database</h1>

      <p>
          To access the database use <a href="http://linkeddatafragments.org/">Linked
          Data Fragments</a> server at <a href="https://licensedb.org/data/">licensedb.org/data/</a>.
      </p>
      <p>
          You can also download full database dumps from
          <a href="https://licensedb.org/dl/">licensedb.org/dl/</a>.
      </p>

      <h2>Adding or correcting data in the database</h2>

      <p>
          If you want to add an entry to the database, or edit an existing entry in the
          database, please fork or clone the <a
          href="https://github.com/warpr/licensedb/">repository on github</a>.  The file
          <code>EDITING.txt</code> in the project root contains instructions and guidelines
          for adding data to the database.
      </p>

